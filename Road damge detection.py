def load_curves(run_name):
    csv_path = PROJECT / run_name / "results.csv"
    if not csv_path.exists():
        return None
    df = pd.read_csv(csv_path)
    cols = {
        "epoch": "epoch",
        "val_box_map": "val/box/mAP50-95",
        "val_seg_map": "val/seg/mAP50-95",
    }
    keep = {k:v for k,v in cols.items() if v in df.columns}
    if not keep:
        return None
    out = df[list(keep.values())].rename(columns={v:k for k,v in keep.items()})
    return out

