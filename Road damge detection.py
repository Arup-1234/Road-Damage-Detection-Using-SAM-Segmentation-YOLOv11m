# Normalize coordinates to [0,1]
def _norm(v, maxv):
    v = max(0.0, min(float(v), float(maxv)))
    return v / float(maxv) if maxv else 0.0

# Write rectangle polygon into YOLO segmentation format
def write_rect_poly(txt_path, cls_id, xmin, ymin, xmax, ymax, w, h):
    poly = [
        _norm(xmin, w), _norm(ymin, h),
        _norm(xmax, w), _norm(ymin, h),
        _norm(xmax, w), _norm(ymax, h),
        _norm(xmin, w), _norm(ymax, h),
    ]
    txt_path.parent.mkdir(parents=True, exist_ok=True)
    with open(txt_path, "a", encoding="utf-8") as f:
        f.write(str(cls_id) + " " + " ".join(f"{p:.6f}" for p in poly) + "\n")

