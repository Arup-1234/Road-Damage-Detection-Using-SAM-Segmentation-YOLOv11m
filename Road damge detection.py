def clamp_xyxy(xmin, ymin, xmax, ymax, w, h):
    xmin = max(0, min(int(round(xmin)), w-1))
    ymin = max(0, min(int(round(ymin)), h-1))
    xmax = max(0, min(int(round(xmax)), w-1))
    ymax = max(0, min(int(round(ymax)), h-1))
    if xmax <= xmin or ymax <= ymin:
        return None
    return [xmin, ymin, xmax, ymax]

def expand_box(xyxy, w, h, margin=0.10):
    xmin, ymin, xmax, ymax = xyxy
    bw, bh = xmax - xmin, ymax - ymin
    return clamp_xyxy(xmin - bw*margin, ymin - bh*margin,
                      xmax + bw*margin, ymax + bh*margin, w, h)

def mask_to_poly_norm(mask_bool):
    h, w = mask_bool.shape[:2]
    cnts, _ = cv2.findContours((mask_bool*255).astype(np.uint8),
                               cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
    if not cnts:
        return None
    cnt = max(cnts, key=cv2.contourArea)
    eps = 0.0015 * cv2.arcLength(cnt, True)
    cnt = cv2.approxPolyDP(cnt, eps, True).reshape(-1, 2)
    if len(cnt) < 4:
        return None
    return [coord for x, y in cnt for coord in (x / w, y / h)]

def rect_poly_norm(xyxy, w, h):
    xmin, ymin, xmax, ymax = xyxy
    return [
        xmin / w, ymin / h,
        xmax / w, ymin / h,
        xmax / w, ymax / h,
        xmin / w, ymax / h,
    ]

