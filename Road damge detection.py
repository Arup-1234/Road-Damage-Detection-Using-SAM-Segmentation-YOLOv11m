def draw_yolo_seg_polys(img_bgr, label_txt_path: Path, color=(0,255,0)):
    h, w = img_bgr.shape[:2]
    if not label_txt_path.exists():
        return img_bgr
    for line in label_txt_path.read_text().strip().splitlines():
        parts = line.split()
        if len(parts) < 9:  # cls + x1 y1 x2 y2 x3 y3 x4 y4 ...
            continue
        cls_id = int(float(parts[0]))
        coords = list(map(float, parts[1:]))
        pts = np.array([(coords[i]*w, coords[i+1]*h) for i in range(0, len(coords), 2)], dtype=np.int32)
        # draw filled translucent polygon + edge
        overlay = img_bgr.copy()
        cv2.fillPoly(overlay, [pts], color)
        img_bgr = cv2.addWeighted(overlay, 0.25, img_bgr, 0.75, 0)
        cv2.polylines(img_bgr, [pts], isClosed=True, color=color, thickness=2)
        cx, cy = int(np.mean(pts[:,0])), int(np.mean(pts[:,1]))
        cv2.putText(img_bgr, CLASS_NAMES[cls_id], (cx, cy), cv2.FONT_HERSHEY_SIMPLEX, 0.6, (0,0,255), 2, cv2.LINE_AA)
