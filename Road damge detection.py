def stems_in_dir(d):
    return {p.stem for p in d.iterdir() if p.suffix==".txt"}

def image_dir_score(img_dir, label_stems):
    if not img_dir or not img_dir.exists(): return -1
    stems = {p.stem for p in img_dir.rglob("*") if p.suffix in IMG_EXTS}
    return len(stems & label_stems)

